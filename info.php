        <h2>PILONIDAL SINUS</h2>
        <p>A pilonidal sinus is a small cyst or abscess that occurs in the cleft at the top of the buttocks.it usually contains hair, dirt and debris. It can cause severe pain and can often become infected. If infected it may ooze pus and blood and have a foul odour.</p>
        <p>Pilonidal sinus mostly affects men and is also common in young adults. It’s also more common in people who sit a lot, like cab drivers.</p>
        <p>Activities that cause friction, like sitting, can force the hair growing in the area to burrow back under the skin. The body considers this hair foreign and launches an immune response against it, similar to how it would react when dealing with a splinter. This immune response forms the cyst around your hair.</p>

